package LamdbaPractice.Example;

public class Player {
    int height;
    int age;
    int weight;
    int number;
    String type;
    String name;

    public Player(int height, int age, int weight, int number, String type, String name){
        this.height = height;
        this.age = age;
        this.weight = weight;
        this.number = number;
        this.type = type;
        this.name = name;
    }


}
